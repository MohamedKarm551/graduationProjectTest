            <p style="direction: rtl;">استراتيجيات التعليم والتعلم تم تحديد القواعد والوسائل التي تجعل المتعلم داخل الكلية في ظل نظام الساعات المعتمدة قادرة على معالجة المعلومات واسترجاعها، والتفكير تفكيرة منطقية مستقلا بحيث يتحقق التركيز على المتعلم باعتباره المنتج الأساسي للمؤسسة ليتعلم لذاته فيصبح متعلما بذاته، يتطلب ذلك تنمية مهارات التفكير المتعددة، وجوانب الشخصية كلها، أما عضو هيئة التدريس فدوره يتحدد في أنه المخطط والمنفذ والميسر والمنظم بما له من خبرات علمية وأكاديمية ومهارات تعينه على تحقيق أهدافه، مستغلا في ذلك الزمن المحدد للمحاضرات النظرية والدروس العملية والساعات المكتبية. .</p>
            <p style="direction: rtl;">&nbsp;أهداف الاستراتيجيات:</p>
            <ol>
                <li style="direction: rtl; text-align: right;">تشجيع الطلاب على إبداء رأيهم.</li>
                <li style="direction: rtl; text-align: right;">تعويد الطلاب على التفكير السليم.&nbsp;</li>
                <li style="direction: rtl; text-align: right;">القدرة على إعطاء تفسير للحلول التي توصلوا إليها.</li>
                <li style="direction: rtl; text-align: right;">تشجيع الطلاب على البحث عن حلول كثيرة ومتنوعة.</li>
                <li style="direction: rtl; text-align: right;">تشجيع الطلاب على الوصول للتعميمات والنظريات والقواعد والمسلمات بأنفسهم بطريقة الاكتشاف.</li>
                <li style="direction: rtl; text-align: right;">شيوع جو من الديمقراطية وحريه إبداء الرأي بين الطلاب.</li>
                <li style="direction: rtl; text-align: right;">تنويع أساليب التدريس المستخدمة لتحقيق الأهداف المرجوة.</li>
